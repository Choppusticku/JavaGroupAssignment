    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (customer == null) {
            JOptionPane.showMessageDialog(null, "Please first select any customer to populate fields and update");
        }else{
            if (userID.getText().isEmpty() || usernameT.getText().isEmpty() || credits.getText().isEmpty() 
                || address.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No Field should be empty");
        }else{
            
            //    public Customer(String address, double credit, ArrayList<Order> orderHistory, String userId, String username, String password, UserType userType) {

            
           customer.setUserId(userID.getText());
           customer.setAddress(address.getText());
           customer.setCredit(Double.parseDouble(credits.getText()));
            CustomerDatabase.updateCustomer(customer);
            
            userID.setText("");
            usernameT.setText("");
            address.setText("");
            credits.setText("");
            usernameT.setEditable(true);
            customer = null;
            table.clearSelection();
            showData();
            JOptionPane.showMessageDialog(null, "Successfully updated Customer");
            
            
        }
        
    }
    }//GEN-LAST:event_jButton2ActionPerformed

