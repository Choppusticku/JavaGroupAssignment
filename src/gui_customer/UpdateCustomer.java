    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int i = table.getSelectedRow();
        if (i != -1) {
            double creditss = (double) table.getValueAt(i, 3);
            userID.setText((String) table.getValueAt(i, 0));
            usernameT.setText((String) table.getValueAt(i, 1));
            address.setText((String) table.getValueAt(i, 2));
            credits.setText(creditss+"");
            customer = CustomerDatabase.getCustomer(usernameT.getText());
            usernameT.setEditable(false);
        }
        
    }//GEN-LAST:event_tableMouseClicked

