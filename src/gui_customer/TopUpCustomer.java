    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (table.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Please select customer to top up");
        }else{
            String customerID = (String) table.getValueAt(0, 1);
            
            Customer customer = CustomerDatabase.getCustomer(customerID);
            if (credits.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please add some credits");
            }else{
                double credit = Double.parseDouble(credits.getText());
                customer.setCredit(credit + customer.getCredit());
                CustomerDatabase.updateCustomer(customer);
                credits.setText("");
                showData();
                JOptionPane.showMessageDialog(null, "Successfully top up customer");
            }
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

