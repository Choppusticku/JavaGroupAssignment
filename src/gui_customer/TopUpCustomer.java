    void showData(){
        model.getDataVector().removeAllElements();
        table.revalidate();
        for (Customer cust : CustomerDatabase.getAllCustomers()) {
            model.addRow(new Object[]{cust.getUserId(),cust.getUsername(),cust.getAddress(),cust.getCredit()});
        }
    }
