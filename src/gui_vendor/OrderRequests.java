    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int i = table.getSelectedRow();
        if (i != -1) {
            String orderID = (String) table.getValueAt(i, 0);
            order = OrderDatabase.getOrderbyOrderID(orderID);
            items.setText("");
            for (MenuItem item : order.getItems()) {
                items.append(item.getItemName() + "\n");
            }
            if (!order.isDeliveryRequested()) {
                deliveryCombo.setEditable(false);
            }

        }

    }//GEN-LAST:event_tableMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        order.setStatus(OrderStatus.ACCEPTED);
        double result = 0;

        if (order.isDeliveryRequested()) {
            if (deliveryCombo.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(null, "Please select delivery runner");
            } else {
                DeliveryRunner deliveryRunner = DeliveryRunnerDatabase.getdDeliveryRunner(deliveryCombo.getSelectedItem().toString());
                deliveryRunner.assignNewOrder(order);
                deliveryRunner.setEarnings(deliveryRunner.getEarnings() + result);

                double deliveryPrevEarn = DeliveryRunnerDatabase.getEarning(deliveryRunner.getUsername());
                DeliveryRunnerDatabase.createOrUpdateRunnerEarning(deliveryRunner.getUsername(), deliveryPrevEarn + result);
                deliveryRunner.setEarnings(deliveryPrevEarn + result);

                DeliveryRunnerDatabase.updateRunner(deliveryRunner);
               
                DeliveryRunnerDatabase.addAssignedOrder(deliveryRunner.getUsername(), order.getOrderId());

            }
        }
        double vendorEarning = order.getTotal() - result;

        OrderDatabase.updateOrder(order);
        double prevEarn = VendorDatabase.getEarning(vendor.getUsername());

        VendorDatabase.createOrUpdateVendorEarning(vendor.getUsername(), vendorEarning + prevEarn);

        this.dispose();
        new VendorDashboard(vendor).setVisible(true);
        JOptionPane.showMessageDialog(null, "Order Accepted");
    }//GEN-LAST:event_jButton1ActionPerformed

