    void showData() {
        vendor = VendorDatabase.getVendor("vendor1122");

        model.getDataVector().removeAllElements();
        table.revalidate();

        for (MenuItem item : vendor.getMenu().getItems()) {
            model.addRow(new Object[]{item.getItemName(), item.getPrice()});
        }
    }
