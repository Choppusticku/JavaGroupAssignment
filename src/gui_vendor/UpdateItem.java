    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (menuItem == null) {
            JOptionPane.showMessageDialog(null, "Please first select any menu item to update");
        }else{
            if (name.getText().isEmpty() || price.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Error! No field should be empty");
            }else{
                
                vendor.updateMenuItem(menuItem, new MenuItem(name.getText(), Double.parseDouble(price.getText())));
                VendorDatabase.updateVendor(vendor);
                showData();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int i = table.getSelectedRow();
        if (i != -1) {
            String name1 = (String) table.getValueAt(i, 0);
            double price1 = (double) table.getValueAt(i, 1);
            menuItem = new MenuItem(name1, price1);
            name.setText(name1);
            this.price.setText(price1+"");
        }
    }//GEN-LAST:event_tableMouseClicked

