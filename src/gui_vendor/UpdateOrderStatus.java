    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int i = table.getSelectedRow();
        if (i == -1) {
            JOptionPane.showMessageDialog(null, "Please select any order to update");
        } else {
            String orderID = (String) table.getValueAt(i, 0);
            Order order = OrderDatabase.getOrderbyOrderID(orderID);
            order.setStatus(OrderStatus.valueOf(status.getSelectedItem().toString()));
            OrderDatabase.updateOrder(order);
            showData();
            JOptionPane.showMessageDialog(null, "Order Status successfully updated");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

