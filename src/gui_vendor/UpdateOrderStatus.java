    void showData() {
        model.getDataVector().removeAllElements();
        table.revalidate();
        vendor = VendorDatabase.getVendor(vendor.getUsername());

        model.getDataVector().removeAllElements();
        table.revalidate();

        for (Order order : OrderDatabase.getOrdersByVendorId(vendor.getUsername())) {
            model.addRow(new Object[]{order.getOrderId(), order.getCustomer().getUsername(), order.getVendor().getUsername(), order.getTotal(), order.isDeliveryRequested(), order.getStatus()});

        }
    }

